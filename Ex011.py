Altura = float(input('Altura da parede: '))
Largura = float(input('Largura da parede: '))
Área = Altura * Largura
print('A dimensão da parede é {} x {} e sua área é de {}m² '.format(Altura, Largura, Área))
Tinta = Área/2
print('Para pintar está parede, você irá precisar de {}L de tinta'.format(Tinta))